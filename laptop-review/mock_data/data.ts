  // Function to get a laptop by ID
  export function getLaptopById(id: string) {
    return laptops.find((laptop) => laptop.id === id) || null
  }
  
  // Function to search laptops by name
  export function searchLaptops(query: string) {
    if (!query) return []
    const lowerQuery = query.toLowerCase()
    return laptops.filter((laptop) => laptop.name.toLowerCase().includes(lowerQuery))
  }
  
